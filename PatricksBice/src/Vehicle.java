
public class Vehicle {
	String name;
	int horsePower;
	int numberOfPassengers;
	String fuelType;
	
	Vehicle(String setName, int setHorsePower, int setNumberOfPassengers, String setFuelType) {
		name = setName;
		horsePower = setHorsePower;
		numberOfPassengers = setNumberOfPassengers;
		fuelType = setFuelType;
	}
}
